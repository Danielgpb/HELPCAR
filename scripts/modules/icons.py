def get_icon(name, variant='standard'):
    """
    Retourne le code SVG d'une icône

    Args:
        name (str): Nom de l'icône (ex: 'car', 'map-pin')
        variant (str): Variante à utiliser ('standard' ou 'lucide')

    Returns:
        str: Code SVG inline ou chaîne vide si non trouvé
    """
    if variant == 'lucide':
        return ICONS_LUCIDE.get(name, '')
    return ICONS_STANDARD.get(name, '')


def get_all_icon_names():
    """
    Retourne la liste de toutes les icônes disponibles

    Returns:
        dict: Dictionnaire avec les clés 'standard' et 'lucide'
    """
    return {
        'standard': list(ICONS_STANDARD.keys()),
        'lucide': list(ICONS_LUCIDE.keys())
    }
